# 정수, integer, int, 산술 연산

a = 3
b = 7

print(a + b)
print(a - b)

print(a * b)
print(a ** b)

print(a / b)
print(a // b)	# 몫 연산

print(a % b)	# 나머지 연산 (modulo) 
print('-----------------------------')

print(a | b)
print(a & b)
print(a >> b)
print(a << b)

print(a == b)
print(a != b)

print(a < b)
print(a > b)

print(a <= b)
print(a >= b)

print('-----------------------------')

print(bool(a))
print(bool(b))
print(bool(0))

